package kr.co.nimo.showroom;

public class ShowRoomVO {
	private String show_name;
	private String show_furs_code;
	private String show_img;
	private String show_detail;
	private String show_date;
	private String show_category;

	public String getShow_name() {
		return show_name;
	}

	public void setShow_name(String show_name) {
		this.show_name = show_name;
	}

	public String getShow_furs_code() {
		return show_furs_code;
	}

	public void setShow_furs_code(String show_furs_code) {
		this.show_furs_code = show_furs_code;
	}

	public String getShow_img() {
		return show_img;
	}

	public void setShow_img(String show_img) {
		this.show_img = show_img;
	}

	public String getShow_detail() {
		return show_detail;
	}

	public void setShow_detail(String show_detail) {
		this.show_detail = show_detail;
	}

	public String getShow_date() {
		return show_date;
	}

	public void setShow_date(String show_date) {
		this.show_date = show_date;
	}

	public String getShow_category() {
		return show_category;
	}

	public void setShow_category(String show_category) {
		this.show_category = show_category;
	}

}
